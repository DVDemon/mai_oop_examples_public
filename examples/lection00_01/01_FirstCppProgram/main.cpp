// Однострочный комментарий - начинается с // и действует до конца строки
// Многострочный комментарий начинается с /* и заканчивается */

// Директива препроцессора #include подключает библиотеку ввода-вывода
// <iostream> содержит функции для работы с консолью (cout, cin, endl)
#include <iostream>

/*
 * ФУНКЦИЯ MAIN - ТОЧКА ВХОДА В ПРОГРАММУ
 * 
 * Каждая программа на C++ должна иметь функцию main()
 * Выполнение программы начинается именно с этой функции
 */
int main(int argc, char * argv[]){
    // Параметры функции main:
    // argc (argument count) - количество аргументов командной строки
    // argv (argument vector) - массив строк с аргументами командной строки
    
    // Альтернативные способы объявления функции main (закомментированы):
    // auto main() -> int {    // Современный C++11+ синтаксис с auto
    // int main(){             // Упрощенная версия без параметров

    /*
     * ВЫВОД ДАННЫХ НА КОНСОЛЬ
     * 
     * std::cout - объект для вывода данных в стандартный поток вывода (консоль)
     * << - оператор вставки (stream insertion operator)
     * "строка" - строковый литерал (текст в кавычках)
     * std::endl - манипулятор, который выводит символ новой строки и сбрасывает буфер
     */
    std::cout << "Number1" << std::endl;  // Выводим "Number1" и переходим на новую строку
    std::cout << "Number2" << std::endl;  // Выводим "Number2" и переходим на новую строку
    std::cout << "Number3" << std::endl;  // Выводим "Number3" и переходим на новую строку

    /*
     * ЗАВЕРШЕНИЕ ПРОГРАММЫ
     * 
     * return 0; - возвращает значение 0 операционной системе
     * 0 означает успешное завершение программы
     * Любое ненулевое значение означает ошибку
     */
    return 0;
}

