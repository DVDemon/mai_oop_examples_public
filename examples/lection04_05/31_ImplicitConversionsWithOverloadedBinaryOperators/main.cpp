#include <iostream>
#include "number.h"

int main()
{
    // Создаем объект Number с начальным значением 22
    Number n1(22);
    std::cout << "n1 : " << n1 << std::endl;

    // Демонстрация неявного преобразования для ПРАВОГО операнда
    // Целое число 2 неявно преобразуется в Number(2)
    std::cout << "n1 + 2  : " << (n1 + 2) << std::endl;

    // Демонстрация неявного преобразования для ЛЕВОГО операнда
    // Это работает только потому, что operator+ объявлен как friend-функция!
    // Целое число 2 неявно преобразуется в Number(2)
    std::cout << "2 + n1 : " << (2 + n1) << std::endl;

    // Ещё один пример: вычитание с неявным преобразованием левого операнда
    // 42 преобразуется в Number(42), затем вычитается n1
    std::cout << "42 - n1 : " << (42 - n1) << std::endl;

    return 0;
}