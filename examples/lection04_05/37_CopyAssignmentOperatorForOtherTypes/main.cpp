#include <iostream>
#include "point.h"
#include "car.h"

int main()
{
    // Создаем точку с координатами (10, 10) и данными 10
    Point p1(10, 10, 10);
    
    // Создаем автомобиль красного цвета со скоростью 200.0
    Car c1("red", 200.0);

    // Демонстрация оператора присваивания для другого типа
    // ВАЖНО: Это НЕ стандартный оператор присваивания
    // Вызывается operator=(const Car&) вместо operator=(const Point&)
    p1 = c1;

    std::cout << "p1 : " << p1 << std::endl;  // Результат: координаты и данные = 200

    return 0;
}