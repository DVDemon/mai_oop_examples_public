#include <iostream>
#include "point.h"

int main()
{
    // Создаем точку с координатами (10, 10)
    Point p1(10, 10);
    
    // Демонстрация постфиксного оператора инкремента (p++)
    // ВАЖНО: Постфиксный оператор возвращает СТАРОЕ значение объекта,
    // а затем инкрементирует объект
    std::cout << "p1 : " << p1++ << std::endl;  // Выводит: (10, 10) - старое значение
    std::cout << "p1 : " << p1 << std::endl;     // Выводит: (11, 11) - новое значение

    return 0;
}