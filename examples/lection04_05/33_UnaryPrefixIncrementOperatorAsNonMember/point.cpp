#include <cmath>
#include "point.h"

// Вычисление расстояния от начала координат (0, 0) до точки
// Используется формула Евклидова расстояния: d = sqrt((x - 0)² + (y - 0)²)
double Point::length() const
{
    return sqrt(pow(m_x - 0, 2) + pow(m_y - 0, 2) * 1.0);
}

// Реализация префиксного оператора инкремента как внешней friend-функции
// ВАЖНО: Это НЕ метод класса, а внешняя функция, объявленная как friend
// 
// Преимущества friend-функции:
// 1. Симметричность: можно писать как ++p, так и operator++(p)
// 2. Доступ к приватным членам через friend-объявление
// 3. Демонстрация альтернативного подхода к перегрузке операторов
void operator++(Point& operand)
{
    ++(operand.m_x);  // Увеличиваем координату x на 1
    ++(operand.m_y);  // Увеличиваем координату y на 1
}
