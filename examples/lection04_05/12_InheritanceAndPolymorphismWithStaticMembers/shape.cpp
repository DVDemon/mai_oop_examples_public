#include "shape.h"

// ИНИЦИАЛИЗАЦИЯ статического члена класса Shape
// ВАЖНО: статические члены должны быть определены вне класса
int Shape::m_count{0};

// Конструктор с параметрами - увеличивает счетчик объектов
Shape::Shape(std::string_view description)
    : m_description(description)
{
    // При создании объекта увеличиваем статический счетчик
    ++m_count;
}

// Конструктор по умолчанию - делегирует вызов конструктору с параметрами
Shape::Shape()
    : Shape("NoDescription")
{
}

// Деструктор - уменьшает счетчик объектов при уничтожении
Shape::~Shape()
{
    // При уничтожении объекта уменьшаем статический счетчик
    --m_count;
}

